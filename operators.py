# Arithmetic Operators - Perform simple
x = 45
y = 21

print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y) # Modulus - Returns the remainder

# Comparison operators
a = 56
b = 2
print(a < b)  # less to
print(a > b)  # greater to
print(a == b)  # equal to
print(a <= b)  # less or equal to
print(a >= b)  # greater or equal to
print(a != b)  # not equal to

# Assignment Operators
z = 6
print(z)

num = 2
num %= 10
print(num)

# Logical Operators
num1 = 5
num2 = 10
print(num1 < num2 and num1 == num2)
print(num1 < num2 or num1 == num2)
print(not(num1< num2 and num1 == num2))

